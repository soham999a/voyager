// Space-related utilities
export function calculateOrbitalVelocity(altitude: number): number {
  // Simplified calculation for circular orbit
  const earthRadius = 6371; // km
  const gravitationalParameter = 398600; // km³/s²
  const orbitalRadius = earthRadius + altitude;
  return Math.sqrt(gravitationalParameter / orbitalRadius) * 3.6; // km/h
}

export function calculateOrbitalPeriod(altitude: number): number {
  // Simplified calculation for circular orbit in minutes
  const earthRadius = 6371; // km
  const gravitationalParameter = 398600; // km³/s²
  const orbitalRadius = earthRadius + altitude;
  return (2 * Math.PI * Math.sqrt(Math.pow(orbitalRadius, 3) / gravitationalParameter)) / 60;
}

export function getTimeZoneOffset(): string {
  const offset = new Date().getTimezoneOffset();
  const hours = Math.floor(Math.abs(offset) / 60);
  const minutes = Math.abs(offset) % 60;
  const sign = offset > 0 ? '-' : '+';
  return `UTC${sign}${hours.toString().padStart(2, '0')}:${minutes.toString().padStart(2, '0')}`;
}

// Local storage utilities
export function setLocalStorage(key: string, value: any): void {
  try {
    localStorage.setItem(key, JSON.stringify(value));
  } catch (error) {
    console.error('Error saving to localStorage:', error);
  }
}

export function getLocalStorage<T>(key: string, defaultValue: T): T {
  try {
    const item = localStorage.getItem(key);
    return item ? JSON.parse(item) : defaultValue;
  } catch (error) {
    console.error('Error reading from localStorage:', error);
    return defaultValue;
  }
}

export function removeLocalStorage(key: string): void {
  try {
    localStorage.removeItem(key);
  } catch (error) {
    console.error('Error removing from localStorage:', error);
  }
}
